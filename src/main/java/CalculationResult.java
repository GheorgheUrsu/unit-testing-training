import java.awt.*;

public class CalculationResult {

    private double number;
    private Color color;

    //getters & setters
    public double getNumber() {
        return number;
    }

    public void setNumber(final double number) {
        this.number = number;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(final Color color) {
        this.color = color;
    }
}
